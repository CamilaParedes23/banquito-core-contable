CREATE TABLE CUENTA_CONTABLE (
  ID INT AUTO_INCREMENT NOT NULL,
  CODIGO_CONTABLE VARCHAR(30) NOT NULL,
  NOMBRE VARCHAR(150) NOT NULL,
  CLASE VARCHAR(20) NOT NULL,
  NATURALEZA_SALDO VARCHAR(15) NOT NULL,
  TIPO_CUENTA VARCHAR(15) NOT NULL,
  CUENTA_PADRE_ID INT NULL,
  NIVEL INT NOT NULL,
  PERMITE_MOVIMIENTOS TINYINT(1) NOT NULL DEFAULT 0,
  SALDO_ACTUAL DECIMAL(19,2) NOT NULL DEFAULT 0.00,
  ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVA',
  FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  VERSION INT NOT NULL DEFAULT 0,
  CONSTRAINT PK_CUENTA_CONTABLE PRIMARY KEY (ID),
  CONSTRAINT UK_CUENTA_CONTABLE_CODIGO UNIQUE (CODIGO_CONTABLE),
  CONSTRAINT FK_CUENTA_CONTABLE_PADRE FOREIGN KEY (CUENTA_PADRE_ID) REFERENCES CUENTA_CONTABLE(ID) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT CK_CUENTA_CONTABLE_CLASE CHECK (CLASE IN ('ACTIVO','PASIVO','PATRIMONIO','INGRESO','GASTO')),
  CONSTRAINT CK_CUENTA_CONTABLE_NAT CHECK (NATURALEZA_SALDO IN ('DEUDORA','ACREEDORA')),
  CONSTRAINT CK_CUENTA_CONTABLE_TIPO CHECK (TIPO_CUENTA IN ('ESTRUCTURAL','DETALLE')),
  CONSTRAINT CK_CUENTA_CONTABLE_ESTADO CHECK (ESTADO IN ('ACTIVA','INACTIVA')),
  CONSTRAINT CK_CUENTA_CONTABLE_MOV CHECK ((TIPO_CUENTA='DETALLE' AND PERMITE_MOVIMIENTOS=1) OR (TIPO_CUENTA='ESTRUCTURAL' AND PERMITE_MOVIMIENTOS=0))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Plan de cuentas jerarquico';

