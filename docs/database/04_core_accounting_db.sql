CREATE VIEW V_CUADRE_ASIENTO_CONTABLE AS
SELECT a.ID AS ASIENTO_ID, a.UUID_ASIENTO, a.FECHA_CONTABLE, a.ESTADO,
       SUM(CASE WHEN d.TIPO_MOVIMIENTO='DEBITO' THEN d.MONTO ELSE 0 END) AS TOTAL_DEBITOS,
       SUM(CASE WHEN d.TIPO_MOVIMIENTO='CREDITO' THEN d.MONTO ELSE 0 END) AS TOTAL_CREDITOS,
       CASE WHEN SUM(CASE WHEN d.TIPO_MOVIMIENTO='DEBITO' THEN d.MONTO ELSE 0 END) =
                 SUM(CASE WHEN d.TIPO_MOVIMIENTO='CREDITO' THEN d.MONTO ELSE 0 END)
            THEN 'CUADRADO' ELSE 'DESCADRADO' END AS ESTADO_CUADRE
FROM ASIENTO_CONTABLE a
JOIN DETALLE_ASIENTO_CONTABLE d ON d.ASIENTO_CONTABLE_ID = a.ID
GROUP BY a.ID, a.UUID_ASIENTO, a.FECHA_CONTABLE, a.ESTADO;

CREATE INDEX IDX_ASIENTO_FECHA ON ASIENTO_CONTABLE(FECHA_CONTABLE, ESTADO);
CREATE INDEX IDX_ASIENTO_CORRELACION ON ASIENTO_CONTABLE(UUID_CORRELACION);
CREATE INDEX IDX_DETALLE_CUENTA ON DETALLE_ASIENTO_CONTABLE(CUENTA_CONTABLE_ID);
CREATE INDEX IDX_OUTBOX_ACCOUNTING_ESTADO ON OUTBOX_EVENT(ESTADO, FECHA_CREACION);

